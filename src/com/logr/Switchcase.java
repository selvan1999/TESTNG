package com.logr;

public class Switchcase {
  public static void main (String [] args) {

  int popocorn =400;
  
  switch (popocorn) {
  
  case 150:
  
  System.out.println("small size popocorn");

  break;
  
  case 250:
  
 System.out.println("medium size popocorn");
  
  break;
  case 400:
  
  System.out.println("jumbo size popocorn ");
  break;
  
  default:  
  
  System.out.println ("invalid data");
  
  }
  
}
  
}
